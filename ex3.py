# Print statement
print("I will now count my chickens:")

# Prints number of hens as a float
print("Hens", 25 + 30 / 6)

# Prints number of roosters.  This actually returns a remainder.
print("Roosters", float(100 - 25 * 3 % 4))

# Print statement.
print("Now I will count the eggs:")

# Prints result as a float.
print(3 + 2 + 1 - 5 + 4 % 2 - 1 / 4 + 6)

# Asks a question.
print("Is it true that 3 + 2 < 5 - 7?")

# Actually prints an answer for the above question.
print(float(3 + 2 < 5 - 7))

# Prints the question and the answer.
print("What is 3 + 2?", 3.0 + 2)
print("What is 5 - 7?", 5.0 - 7)

# Print statement.
print("Oh, that's why it's False.")

# Print statement.
print("How about some more.")

# Prints the result of a comparison.
print("Is it greater?", 5.0 > -2.0)
print("Is it greater or equal?", 5.0 >= -2.0)
print("Is it less or equal?", 5.0 <= -2.0)
